package org.example;

import java.util.Arrays;

//Сортировка вставками - это алгоритм сортировки, который работает
//        путем вставки каждого элемента в правильную позицию в отсортированном
//        списке. Алгоритм начинается с сравнения второго элемента со всеми элементами,
//        которые находятся перед ним, и если он меньше предыдущего элемента, то он перемещается
//        влево до тех пор, пока не найдет свое место.
//
//        Вот пример кода на Java для сортировки вставками:
public class InsertionSort {
    public static void sort(int[] arr) {
        int n = arr.length;
        for (int i = 1; i < n; ++i) {
            int key = arr[i];
            int j = i - 1;
            while (j >= 0 && arr[j] > key) {
                arr[j + 1] = arr[j];
                j = j - 1;
            }
            arr[j + 1] = key;
        }
    }

    public static void main(String[] args) {
        int[] arr = {3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5};
        System.out.println("Original array: " + Arrays.toString(arr));
        sort(arr);
        System.out.println("Sorted array: " + Arrays.toString(arr));
    }
}
//    В этом коде мы проходим по массиву, начиная с второго элемента (индекс 1) и
//        сохраняем его в переменной key. Затем мы проходимся по всем предыдущим элементам
//        и сдвигаем их вправо, пока не найдем правильное место для key. Мы находим это место,
//        когда найдем элемент, который меньше или равен key, и вставляем key после него.
//
//        Вызов метода sort() сортирует массив, переданный ему в качестве аргумента. Например,
//        чтобы отсортировать массив arr, можно использовать следующий код:
