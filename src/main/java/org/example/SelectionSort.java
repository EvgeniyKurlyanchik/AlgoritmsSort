package org.example;

//Сортировка выбором (Selection Sort) - это простой алгоритм сортировки,
//        который на каждой итерации выбирает наименьший элемент из неотсортированной
//        части массива и перемещает его в начало этой части. Этот процесс повторяется,
//        пока все элементы не будут отсортированы.
//
//        Пример кода на Java:


public class SelectionSort {
    public static void main(String[] args) {
        int[] arr = {5, 3, 8, 4, 2};
        int temp, minIndex;
        for(int i=0; i < arr.length-1; i++){
            minIndex = i;
            for(int j=i+1; j < arr.length; j++){
                if(arr[j] < arr[minIndex]){
                    minIndex = j;
                }
            }
            temp = arr[i];
            arr[i] = arr[minIndex];
            arr[minIndex] = temp;
        }
        for(int i=0; i < arr.length; i++){
            System.out.print(arr[i] + " ");
        }
    }
}
//    В этом примере мы используем два вложенных цикла for для поиска
//        наименьшего элемента и его перемещения в начало неотсортированной
//        части массива. Сначала мы выбираем i-й элемент как минимальный элемент
//        и сравниваем его со всеми элементами, начиная с i+1-го. Если мы находим
//        элемент, который меньше текущего минимального элемента, мы обновляем minIndex
//        на индекс этого элемента. После прохода по всем элементам неотсортированной части
//        мы меняем местами минимальный элемент и i-й элемент. Этот процесс повторяется до тех
//        пор, пока все элементы не будут отсортированы.
//
//        На выходе получаем отсортированный массив: 2 3 4 5 8.